    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        StringBuilder sb = new StringBuilder();
        if(jFormattedTextField1.getText().equals("")){
            sb.append("Fullname is required! \n");
        }
        if(jFormattedTextField2.getText().equals("")){
            sb.append("Email is required! \n");
        }
        if(jFormattedTextField3.getText().equals("")){
            sb.append("Username is required! \n");
        }
        String pw = new String(jPasswordField1.getPassword());
        if(pw.equals("")){
            sb.append("Password is required! \n");
        }
        String cf = new String(jPasswordField2.getPassword());
        if(!pw.equals(cf)){
            sb.append("Pass and re-enter password must be same! \n");
        }
        if(sb.length()>0){
            JOptionPane.showMessageDialog(this,sb.toString());
            return;
        }
        JOptionPane.showMessageDialog(this,"You have successfully registered an account!");
        
    }//GEN-LAST:event_jButton2ActionPerformed

